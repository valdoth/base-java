
public class OperateursArithmetiques {

	public static void main(String[] args) {
		
		System.out.println(45+78);
		System.out.println(56.9+5);
		System.out.println(45-76);
		System.out.println(45.7-76.3);
		System.out.println(2*78);
		System.out.println(76/9);
		System.out.println(76./9);
		System.out.println(43.89*34.27);
		
		System.out.println(11%4);
		System.out.println(23%6);
		System.out.println(-11%3);
		System.out.println(-15.2%7.5);
		
		System.out.println(7.9/0);
		
	}

}
